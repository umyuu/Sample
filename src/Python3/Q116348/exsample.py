# -*- coding: utf-8 -*-

#======================================================================
# ライブラリ
#======================================================================
# GUIを扱う
import tkinter

#======================================================================
# クラス定義
#======================================================================
#----------------------------------------------------------------------
#
#----------------------------------------------------------------------

#======================================================================
# 本体処理
#======================================================================
def main() -> None:
    # インスタンス生成
    window = tkinter.Tk()
    # ウィンドウタイトルを決定
    window.title("WORDPRACTICE")
    # ウィンドウの大きさを決定
    window.geometry("800x600")

    # タイトル出力
    font  = ("Helevetice", 32, "bold")
    label = tkinter.Label(window, text="WORDPRACTICE", font=font, bg="red")
    label.pack()
    # プログラムを始める
    window.mainloop()


if __name__ == "__main__":
    main()
